// Membuat sebuah array kosong
let myArray = [];

// Menambahkan nilai ke dalam array
myArray.push("apel");
myArray.push("jeruk");
myArray.push("mangga");

// Menampilkan isi array
console.log(myArray); // Output: [ 'apel', 'jeruk', 'mangga' ]

// Mengakses nilai dalam array berdasarkan indeks
console.log(myArray[0]); // Output: apel
console.log(myArray[1]); // Output: jeruk
console.log(myArray[2]); // Output: mangga
